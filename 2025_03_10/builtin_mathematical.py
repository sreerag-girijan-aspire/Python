# abs()
print(abs(-5))  # Returns 5 (absolute value)

# round()
print(round(3.14159, 2))  # Returns 3.14, rounded to 2 decimal places

# pow()
print(pow(2, 3))  # Returns 8 (2^3)

# min()
print(min(3, 1, 4, 2))  # Returns 1, the smallest number

# max()
print(max(3, 1, 4, 2))  # Returns 4, the largest number

# sum()
print(sum([1, 2, 3]))  # Returns 6, the sum of the list

# divmod()
print(divmod(9, 4))  # Returns (2, 1), quotient and remainder
